void
as_destroy(struct addrspace *as)
{
	int i;
	struct proc *p = curproc;
	(void)p;
	struct pt_entry *page_table = as->page_table;
	for(i = 0; i < N_FRAME; i++){
		freeppages(page_table[i].paddr, 1);
	}
	/* Free process pages stored in swap file */
	swap_free(as);
	/* Close ELF file */
	vfs_close(as->v);
	int spl = splhigh();
	for (i=0; i<NUM_TLB; i++) {
		tlb_write(TLBHI_INVALID(i), TLBLO_INVALID(), i);
	}
	splx(spl);
	kfree(as);
}

void
as_activate(void)
{
	int i, spl;
	struct addrspace *as;
	static struct addrspace *prev_as = NULL;

	as = proc_getas();
	if (as == NULL) {
		return;
	}

	if(prev_as != as){
		/* Disable interrupts on this CPU while frobbing the TLB. */
		spl = splhigh();

		for (i=0; i<NUM_TLB; i++) {
			tlb_write(TLBHI_INVALID(i), TLBLO_INVALID(), i);
		}

		splx(spl);
		inc_TLB_invalid();
	}

	prev_as = as;
}

