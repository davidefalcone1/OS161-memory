struct addrspace *
as_create(void)
{
	struct addrspace *as;
	int i;

	as = kmalloc(sizeof(struct addrspace));
	if (as == NULL) {
		return NULL;
	}

	as->as_vbase1 = 0;
	as->as_npages1 = 0;
	as->as_vbase2 = 0;
	as->as_npages2 = 0;
	as->offset_data_elf = -1;
	as->offset_text_elf = -1;
	

	for(i = 0; i < N_FRAME; i++)
		as->page_table[i].resident = 0;

	return as;
}

